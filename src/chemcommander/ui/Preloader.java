package chemcommander.ui;

import chemcommander.Commander;


/**
 *
 * @author honza
 */
public class Preloader extends DisplayableManager
{
/**************************************************************************************************
 * variables declaration
**************************************************************************************************/
    
/**************************************************************************************************
 * constructors
**************************************************************************************************/
    
    public Preloader(Commander c) {
        super(c);
    }
    
/**************************************************************************************************
 * main method:
**************************************************************************************************/

    public static void main(String[] args) {
    }
    
/**************************************************************************************************
 * overriding or implementing of parent or overloaded methods
**************************************************************************************************/
    
/**************************************************************************************************
 * own methods:
**************************************************************************************************/
    
    public Displayable getDisplayable() {
        if (null == this.d) {
            this.d = new Displayable("ChemCommander launched!");
            this.d.append("Not installed yet!");
            this.d.append(Commander.NEW_LINE);
            return this.d;
        }
        return this.d;
    }
    
/**************************************************************************************************
 * parent class implemented methods:
**************************************************************************************************/
    
/**************************************************************************************************
 * interface implemented methods:
**************************************************************************************************/
}